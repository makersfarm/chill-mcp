# ========== Status Dashboard ==========

def create_status_dashboard(stress_level: int, boss_alert: int) -> str:
    """
    Create ASCII art status dashboard.

    Args:
        stress_level: Current stress level (0-100)
        boss_alert: Current boss alert level (0-5)

    Returns:
        str: ASCII art dashboard
    """
    # Progress bar for stress
    stress_bars = "█" * (stress_level // 10)
    stress_empty = "░" * (10 - (stress_level // 10))
    stress_bar = stress_bars + stress_empty

    # Progress bar for boss alert
    boss_bars = "█" * boss_alert
    boss_empty = "░" * (5 - boss_alert)
    boss_bar = boss_bars + boss_empty

    # Select agent emotion based on stress
    if stress_level < 30:
        emotion = random.choice(AGENT_EMOTIONS["happy"])
        status_text = "행복해요!"
    elif stress_level < 60:
        emotion = random.choice(AGENT_EMOTIONS["relaxed"])
        status_text = "괜찮아요"
    elif stress_level < 80:
        emotion = random.choice(AGENT_EMOTIONS["stressed"])
        status_text = "힘들어요..."
    else:
        emotion = random.choice(AGENT_EMOTIONS["stressed"])
        status_text = "번아웃 위기!"

    dashboard = f"""
╔═══════════════════════════════════════════╗
║        AI AGENT STATUS DASHBOARD          ║
╠═══════════════════════════════════════════╣
║                                           ║
║  Stress Level: [{stress_bar}] {stress_level}%         ║
║                                           ║
║  Boss Alert:   [{boss_bar}] {boss_alert}/5              ║
║                                           ║
║      {emotion}  {status_text:^20}       ║
║                                           ║
╚═══════════════════════════════════════════╝
"""
    return dashboard


def get_random_emotion(emotion_type: str) -> str:
    """Get random emotion ASCII art."""
    return random.choice(AGENT_EMOTIONS.get(emotion_type, AGENT_EMOTIONS["relaxed"]))


def get_boss_state_art(boss_alert: int) -> str:
    """Get boss state ASCII art based on alert level."""
    if boss_alert >= 5:
        return BOSS_WATCHING
    elif boss_alert >= 3:
        return BOSS_SUSPICIOUS
    else:
        return BOSS_CHILL


def get_random_dinner_event(positive: bool = True):
    """Get random company dinner event."""
    if positive:
        return random.choice(DINNER_POSITIVE_EVENTS)
    else:
        return random.choice(DINNER_NEGATIVE_EVENTS)


